// Condicionales

// Valores numéricos
const numero1 = 10;
const numero2 = 20;
const numero3 = 30;

// 1. Imprimir el número mayor de los tres
const numeroMayor = Math.max(numero1, numero2, numero3);
console.log("El número mayor es:", numeroMayor);

// 2. Imprimir el número menor de los tres
const numeroMenor = Math.min(numero1, numero2, numero3);
console.log("El número menor es:", numeroMenor);

